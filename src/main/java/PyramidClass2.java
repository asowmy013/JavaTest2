public class PyramidClass2 {

    public void Pyramid2()
    {

        int k =1;

        for(int i =0 ; i < 4 ;i++)
        {


            for(int j = 1 ; j <= i+1  ; j++){
                System.out.print(k + " ");
                k++;

            }
            System.out.println(" ");
        }

    }

}
